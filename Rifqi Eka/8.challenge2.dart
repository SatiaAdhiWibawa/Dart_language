void main() {
/*
  1. Buatlah Sebuah formula untuk menghitung keliling persegi
  2. Buatlah Sebuah formula untuk menghitung luas lingkaran
  3. Buatlah Sebuah formula untuk menghitung volume balok
*/
  // No.1 (rumus = sisi x sisi)
  int sisi = 10;
  int keliling = sisi * 4;
  print(keliling);

  // No.2 (rumus : V = rxr)
  int jari2 = 10;
  double luas = 3.14 * jari2 * jari2;
  print(luas);

  // No.3 (Rumus = panjang x lebar x tinggi)
  int panjang = 6;
  int lebar = 8;
  int tinggi = 10;
  int volume = panjang * lebar * tinggi;
  print(volume);
}
